// LINGUAGEM COMPILADA X LINGUAGEM INTERPRETADA
// Em uma linguagem compilada, a máquina de destino traduz o programa diretamente. 
// Em uma linguagem interpretada, o código fonte não é traduzido diretamente pela máquina de destino. 
